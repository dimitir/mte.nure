                        Відмінною особливістю НДДКР кафедри була обов'язкова направленість
                        на створення зразків нової вимірювальної техніки та їх впровадження
                        у замовників. Рідкісна хоздоговорна тема закінчувалась без цього.
                        Крім того, експериментальні і досліджувані зразки володіли новизною
                        і захищались авторськими правами, а потім патентами України і Росії,
                        яких отримано співробітниками близько двохсот. Окремі розробки експонувались
                        на виставках досягнень народного господарства України і СССР, а одна
                        із стаціонарних пондеромоторних установок - на міжнародній виставці
                        в Лейпцігу. Кращі з розробок нагороджувались дипломами Пошани й медалями
                        ВДНХ СССР (Кукуш В.Д., Жилков В.С., Волков В.М., Дідик Л.А., Калугін
                        Б.А. та ін.). <br>
