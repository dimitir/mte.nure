                        Роботи по створенню зразкових пондеромоторних
                        і калориметричних ватметрів, а також робочих ватметрів на основі
                        поглинаючої стінки і багатозондового методу отримали найбільший
                        розвиток і визнання в Україні, СНД і зі їх межами. Протягом декількох
                        десятиліть кафедра РВ виконувала замовлення підприємств АН СССР
                        на розробку ватметрів прохідної потужності і комплексного коефіцієнта
                        відбиття для різних типів прискорювачів заряджених частинок. Деякі
                        з робіт були включені в плани важливіших робіт держави, а одна з
                        них виконувалась згідно з установою Ради Міністрів України СССР.
                        <br>
                        <br>
                        Одну з ОКР, виконаних кафедрою спільно з КБ Світловодського радіозаводу,
                        завершено створенням дослідних зразків та підготовкою документації
                        для серійного виробництва ватметрів прохідної потужності для станцій
                        тропосферного зв'язку.
                        <br><br>В наслідок продуктивної наукової
