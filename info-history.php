                        У жовтні 1964 року, в зв'язку з створенням у ВУЗі ряду радіоелектронних
                        спеціальностей, на радіотехничному факультеті для підготовки студентів
                        по метрології і вимірювальній техніці була організована кафедра &quot;Радіовимірювань&quot;
                        зі статусом випускаючої кафедри. Організацію кафедри і завідування
                        нею було доручено доценту Кукушу В.Д., який мав досвід роботи на кафедрі
                        &quot;Радіовимірювань&quot; ХДУ ім. Горького. На кафедрі викладалися
                        метрологічні дисципліни для всіх спеціальностей ВУЗу, проводилися
                        лабораторні роботи, виконувалося дипломне проектування, здійснювалося
                        керівництво практикою. <br>
                        <br>
                        Таким чином, ХІРЕ протягом багатьох років забезпечував кадрами Харківський
                        державний науково-дослідний інститут метрології (ХДНДІМ) та Харківський
                        центр стандартизації, сертифікації та метрології (ХЦСМ), причому ця
                        традиція збереглася і по теперішній час. <br>
                        <br>
                        Кандидатські дисертації захистило в ХІРЕ багато співробітників ХДНДІМ:
                        І.Ф. Павленко, нині заст. директора інституту по науковій роботі;
                        Г.С. Сидоренко, в цей час директор інституту і заст. голови Держкомітету
                        стандартів та інші. Колектив кафедри брав участь в фундаментальних
                        розробках НДІ метрології ВНИИФТРИ і ХДНДІМ. <br>
                        <br>
                        У переліку спеціальностей вищої школи СРСР до 1988 року була відсутня
                        метрологічна спеціальність. Тому кафедра &quot;Радіовимірювань&quot;
                        для того, щоб забезпечити випуск інженерів-метрологів домоглася дозволу
                        МВССО УССР на спеціалізацію однієї групи студентів спеціальності &quot;Радіотехника&quot;
                        та однієї групи спеціальності&quot;Радіофізика&quot;. <br>
                        <br>
                        У 1990 році була відкрита вже метрологічна спеціальність &quot;Метрологія,
                        стандартизація та управління якістю&quot;, яка в 1994 році була перейменована
                        в &quot;Метрологію, стандартизацію та сертифікацію&quot;. З 1997 року
                        по теперішній час спеціальність називається &quot;Метрологія та вимірювальна
                        техніка&quot;. Головою Державної екзаменаційної комісії першого випуску
                        інженерів-метрологів був заст. директора ХЦСМС по метрології В.Н.
                        Чепела.<br>
                        <br>
                        В 1996 році кафедру &quot;Метрології та вимірювальної техніки&quot;
                        очолив д.т.н., проф. Руженцев І.В., який розширив область наукових
                        інтересів кафедри в напрямку моделювання і аналізу електромагнітних
                        хвиль, в тому числі НВЧ - діапазону. Співробітники кафедри почали
                        роботи по електромагнітному моніторингу навколишнього середовища,
                        по використанню мікрохвилевої техніки в промисловості та сільському
                        господарстві. Для метрологічного супроводжування створюваного обладнання
                        було відкрито науково-виробничий випробувальний центр, в якому була
                        акредитована випробувальна лабораторія. Напрямками акредитації є мікрохвилева
                        техніка та програмне забезпечення. Останнє дозволило поставити нові
                        наукові курси з атестації та сертифікації програмних засобів, які
                        виклдаються на ряді факультетів університету. <br>
                        <br>
                        Важливою віхою в постановці учбового процесу з направлення &quot;Метрологія
                        та вимірювальна техніка&quot; став перехід в 1999 році на підготовку
                        бакалаврів-спеціалістів-магістрів. Це дозволило цілеспрямовано готовити
                        майбутніх дослідників, зокрема аспірантів та докторантів. <br>
                        На кафедрі функціонує університетський кабінет стандартизації та сертифікації,
                        який забезпечує як навчальний процес, зокрема курсове та дипломне
                        проектування, так і наукову діяльність ВУЗу. <br>
                        <br>
                        Кафедрою радіовимірювань протягом багатьох років розроблялися засоби
                        вимірювань параметрів сигналів і ліній передачі в діапазонах ОВЧ,
                        УВЧ і НВЧ для ЦКБЄМ, м. Калінінград Московської області, відомого
                        як КБ ім. С.П. Корольова. Для обробки антенних систем космічних літальних
                        апаратів були розроблені і з успіхом використовувались робочі і зразкові
                        ватметри в хвилеводному та коаксіальному виконанні, автоматизовані
                        вимірювальні системи &quot;Москіт&quot; та &quot;АИР&quot; на базі
                        мікро-ЕОМ. <br>
                        <br>
                        З перших днів створення кафедри радіовимірювань була розвернута масштабна
                        діяльність колективу з організації учбово-методичної та наукової роботи,
                        створенню учбових лабораторій і оснащенню їх сучасним обладнанням,
                        включаючи, перш за все, радіовимірювальні прилади. Так, при підтримці
                        ректора інституту проф. Терещенко А.І. тільки з підприємств міністерства
                        електронної промисловості СССР було безкоштовно отримано апаратуру
                        на десятки мільйонів карбованців. При цьому лабораторії поповнювались
                        і приборами за рахунок хоздоговорних робіт. <br>
                        <br>
                        Досвід роботи на заводі ім. Шевченко і на кафедрі радіовимірювань
                        ХДУ завідуючого кафедрою В.Д.Кукуша дозволив в найкоротші терміни
                        визначитися з науковим напрямком робіт. Це дослідження і розробка
                        методів і створення зразкових і робочих засобів вимірювання потужності
                        СВЧ. В більшості фінансування проводилось за рахунок хоздоговорних
                        робіт, в основному підприємствами (НДІ, КБ) міністерств електронної
                        і радіотехнічної промисловості, загального машинобудування, АН и Держстандарту
                        СССР.
