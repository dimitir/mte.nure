                <h5 class="my-text-headline-two-level text-center">"Якість, стандартизація та
                    сетрифікація&quot;(магістр)</h5>
                <div class="describe-vocation">
                    <p>Необхідність підготовки спеціалістів зі спеціальності "Якість, стандартизація та сертифікація"
                        викликана дефіцитом в регіонах України. Кафедра має багатий досвід в підготовці та
                        перепідготовці фахівців зі спеціальності "Якість, стандартизація та сертифікація", підготовка
                        ведеться за найсучаснішими навчальними програмами.’</p>
                    <span>Спеціаліст із спеціальності "Якість, стандартизація та сертифікація" здатен:</span>

                    <ol>
                        <li>
                            розробити документацію на систему якості продукції та послуг;
                        </li>
                        <li>
                            розробити документацію на систему охорони праці та безпеки життєдіяльності людини;
                        </li>
                        <li>розробити документацію на систему загального управління (TQM);</li>
                        <li>розробити документацію на методи контролю і управління якістю продукції та послуг;</li>
                        <li>організувати робу по проведенню сертифікації продукції та систем якості;</li>
                        <li>оцінити рівень якості продукції та послуг;</li>
                        <li>обслуговувати засоби сертифікаційного випробування;</li>
                        <li>проводити сертифікаційні випробування на ЕМС;</li>
                        <li>проводити сертифікаційні випробування радіаційних джерел;</li>
                        <li>проводити сертифікаційні випробування споживчих товарів;</li>
                        <li>проводити сертифікаційні випробування програмної продукції і інформаційних послуг;</li>
                        <li>розробити нормтивну документацію зі стандартизації;</li>
                        <li>проаналізувати та розробити експлуатаційну документацю;</li>
                        <li>забезпечити проведення експерименту та опрацювання даних;</li>
                        <li>користуватися засобами технічних вимірювань;</li>
                        <li>експериментально визначати похибки вимірювань;</li>
                        <li>проводити оцінку похибок вимірювань;</li>
                        <li>визначити невизначеність вимірювань;</li>
                        <li>розробляти схеми інформаційно-вимірювальних систем.</li>
