                <h5 class="my-text-headline-two-level text-center">"Метрологічне забезпечення випробувань та якості
                    продукції"(магістр)</h5>
                <div class="describe-vocation">
                    <p>На сьогоднішній день ефективна співпраця з іншими країнами, участь у міжнародному товарообміні
                        вимагає від України впевненості в надійних і простежуються результати вимірювань, а також взаємного
                        визнання результатів оцінки відповідності продукції. Поява нових технологій в різних галузях
                        промисловості спровокувало зростання видів вимірювань та вимог до їх точності. Метрологічна
                        діяльність в реальному секторі економіки забезпечує створення таких законодавчих, технічних,
                        організаційних і нормативних умов, щоб промисловість і бізнес впевнено діяли в складних і
                        швидкозмінних процесах сучасного світу. Необхідність в метрологічній діяльності і
                        фахівцях-метрологах закріплена в законах України «Про захист прав споживачів», «Про метрологію та
                        метрологічну діяльність», «Про стандартизацію», «Про технічні регламенти та оцінку відповідності» та
                        ін.</p>
                    <span>
                        Випускники, що засвоїли програму «Метрологічне забезпечення випробувань та якості продукції», володіють наступними компетенціями:
                    </span>
                    <ul>
                        <li>здатність до обґрунтування, встановлення, реалізації та контролю норм, правил і вимог до
                            продукції (послуги), технологічних процесів її розробки, виробництва, застосування (споживання),
                            транспортування та утилізації;
                        </li>
                        <li>здатність до розробки метрологічного забезпечення, здійсненню метрологічного контролю і нагляду,
                            що націлені на підтримку єдності вимірювань, високу якість і безпеку продукції (послуги), високу
                            економічну ефективність для виробників і споживачів;
                        </li>
                        <li>здатність до проведення наукових досліджень і розробки складних прикладних проблем в галузі
                            метрології та метрологічного забезпечення.
                        </li>
                    </ul>
                    <p>Отримані компетенції дозволяють випускникам програми працювати в національних органах з метрології –
                        ННЦ «Інститут метрології», стандартизації – Державне підприємство «Український науково-дослідний і
                        навчальний центр проблем стандартизації, сертифікації та якості», акредитації – Національне
                        агентство з акредитації України, в провідних органах з сертифікації (наприклад,
                        Укрметртестстандарт), метрологічних службах підприємств, калібрувальних і випробувальних
                        лабораторіях, органах ринкового і метрологічного нагляду, митній службі.</p>

