                <h5 class="my-text-headline-two-level text-center">"Метрологія та вимірювальна техніка&quot;(магістр)</h5>
                <div class="describe-vocation">
                    <p>
                        Спеціаліст із спеціальності 7.091302 "Метрологія та вимірювальна техніка", спеціалізації
                        "Метрологія,
                        стандартизація та сертифікація у приладобудуванні" забезпечує усі етапи життєвого циклу засобів
                        метрологічного забезпечення, стандартизації та сертифікації:
                    </p>
                    <ul>
                        <li>прогнозування, проектування, виробництва;</li>
                        <li>менеджменту та маркетингу;</li>
                        <li>експлуатації, модернізації, моделювання та оптимізації технологічних процесів і систем
                            виготовлення засобів вимірювання, що містять радіоелектронні, оптоелектронні, ренгеновські і
                            іонноплазмові прилади і пристрої, а також ті, що використовують ЕОМ і системи
                            автоматизованого проектування.
                    </ul>

                    <p>
                        Приймає участь у розробленні, макетуванні, виготовленні, експлуатації, технічному обслуговувані
                        та ремонту технічних і програмних засобів для вимірювання електричних та неелектричних величин.
                    </p>
                    <span>Предметом діяльності спеціаліста є вирішення задач у галузях:</span>

                    <ul>
                        <li>метрологічного забезпечення виробництва;</li>
                        <li>розробки та виготовлення засобів вимірювання;</li>
                        <li>створення та експлуатації еталонів фізичних величин;</li>
                        <li>контролю та випробування продукції виробництва;</li>
                        <li>сертифікації товарів.</li>
                    </ul>

                    <span>Спеціаліст здатен забезпечити:</span>
                    <ul>
                        <li>проектування та виготовлення технічних засобів метрологічного забезпечення виробництва;</li>
                        <li>метрологічну експертизу та нормоконтроль технічної та технологічної документації;</li>
                        <li>створення технічного та нормативного забезпечення сертифікаційних лабораторій;</li>
                        <li>експлуатацію, технічне і метрологічне обслуговування та ремонт засобів вимірювання.</li>
                    </ul>

                    <p>Крім того, спеціаліст добре володіє сучасною офісною та обчислювальною технікою.</p>
                    <p>Інженер з метрології та вимірювальної техніки може працювати в відділах головного метролога, в
                        науково-виробничих, проектно-конструкторських і технологічних організаціях, підприємствах,
                        фірмах та бюро, у науково-дослідних інститутах галузей промисловості, Академії Наук України,
                        Держстандарту України, а також в державних організаціях, що займаються виробництвом,
                        проектуванням, дослідженням та експлуа-тацією засобів вимірювання різноманітного призначення,
                        сертифікацією товарів виробництва.</p>
                    <p>Відповідно до існуючої номенклатури державного класифікатора професій спеціаліст із спеціальності
                        7.091302 "Метрологія та вимірювальна техніка" може займати посади інженера різних категорій,
                        керівників відділів організацій, працювати на посадах в науково-дослідних установах, інститутах
                        Академії наук, підприємствах Держстандарту України, центрах стандартизації та метрології,
                        сертифікаційних лабораторіях, а також комерційних установах від-повідного профілю. Він може
                        займати посади референта, консультанта з питань техніки, менеджера.</p>
                </div>

