                    <p>В рамках освітньої програми “Технічна експертиза” студенти вивчають:</p>

                    <ul>
                        <li>нормативно-правові основи технічної експертизи та технічні регламенти України;</li>
                        <li>сучасні методи менеджменту безпечності харчових продуктів (НАССР, ІSO 22000, IFC,BRC, FSSC),
                            методи
                            та засоби контролю якості та безпечності харчових продуктів;
                        </li>

                        <li>
                            засади безпеки нехарчової продукції, механізми ринкового нагляду і контролю
                            нехарчової
                            продукції;
                        </li>

                        <li>
                            організацію та порядок здійснення споживчої експертизи продукції у відповідності до
                            національного
                            законодавства;
                        </li>

                        <li>
                            метрологічне забезпечення експертизи, випробувань та оцінки відповідності;
                        </li>

                        <li>
                            зміст та порядок проведення технічного аудиту підприємств у відповідності до
                            національного
                            законодавства;
                        </li>

                        <li>
                            правила розробки та проведення технічної експертизи проектної, конструкторської,
                            технологічної,
                            експлуатаційної документації, яка застосовується під час розробці, виробництві,
                            випробуваннях та
                            експлуатації виробів і продукції у всіх секторах економіки і сфері послуг;
                        </li>

                        <li>
                            правила розробки та проведення технічної експертизи технічних регламентів,
                            стандартів та
                            інших
                            нормативних документів;
                        </li>

                        <li>
                            правила розробки, впровадження та функціонування систем менеджменту якості
                            підприємств
                            ISO 9001, ISO
                            22000, OHSAS 18001, ISO
                            14000;
                        </li>

                        <li>
                            організацію та порядок проведення аудитів систем якості, процесів/методів,
                            продукції/послуг ISO
                            19011;
                        </li>

                        <li>
                            сучасні методи експертизи та тестування програмних засобів.

                        </li>

                    </ul>
                    <p>
                        Навчання в рамках освітньої програми &quot;Технічна експертиза&quot; відбуваються у
                        спеціалізованих
                        лабораторіях кафедри: &quot;Лабораторія
                        оцінки відповідності та систем якості&quot;, &quot;Лабораторія тестування та
                        сертифікації
                        програмних
                        засобів&quot;, &quot;Лабораторія стандартизації та
                        технічного регулювання&quot;, &quot;Лабораторія теоретичної метрології&quot;, &quot;Лабораторії
                        інформаційно-вимірювальних систем та інтелектуальних
                        засобів вимірювань&quot;, &quot;Лабораторія вимірювань геометричних величин та їх
                        метрологічного
                        забезпечення&quot; та в наукових лабораторіях
                        Національного наукового центру &quot;Інститут метрології&quot;. </p>

                    <p>
                        Випускники за освітньою програмою &quot;Технічна експертиза&quot; працюють як у
                        державних та
                        приватних
                        компаніях. Це, наприклад,
                        державні органи, що здійснюють ринковий нагляд за дотриманням законів у сфері технічного
                        регулювання,
                        уповноважені організації з
                        проведення експертизи та аудиту, експертні ради та технічні комітети, Державні
                        регіональні
                        науково-виробничі центри стандартизації,
                        метрології та сертифікації, Укрметртестстандарт, Український науково-дослідний і
                        навчальний
                        центр
                        проблем стандартизації, сертифікації
                        та якості. В приватних компаніях випускники за освітньою програмою &quot;Технічна
                        експертиза&quot;
                        працюють та очолюють служби менеджменту
                        якості, відділи технічного контролю, випробувальні та калібрувальні лабораторії, робочі
                        групи НАССР на
                        всіх харчових підприємствах.
                    </p>

                    <p>
                        Уміння та компетенції, якими буде володіти бакалавр за освітньою програмою &quot;Технічна
                        експертиза&quot;:
                    </p>
                    <ol type="1">
                        <li>
                            Володіння методами технічної експертизи (зокрема специфічними: хроматографічний,
                            спектральний,
                            фотоелектро-
                            калориметричний, потенціометричний, реологічний, метод мікроскопування);
                        </li>

                        <li>
                            Вміння розробляти, впроваджувати та підтримувати функціонування систем менеджменту
                            якості
                            підприємств.
                        </li>

                        <li>
                            Застосування засобів вимірювання фізичних величин, включаючи засоби вимірювання
                            показників
                            енергоаудиту;
                        </li>

                        <li>
                            Знання прийомів обробки результатів вимірювань і оцінки їх невизначеності;
                        </li>

                        <li>
                            Основні методи оцінки відповідності параметрів товарів і послуг вимогам нормативної
                            документації;
                        </li>

                        <li>
                            Освоєння нормативної документації в широких областях економіки.
                        </li>
                    </ol>

                    <p>
                        Випускник за освітньою програмою &quot;Технічна експертиза&quot; зможе вирішувати такі
                        завдання:
                    </p>

                    <ol type="1">

                        <li>
                            Виконувати технічну експертизу товарів зарубіжного і вітчизняного виробництва з метою
                            визначення фальсифікації товарів і їх незадовільної якості;
                        </li>

                        <li>
                            Проводить техническую экспертизу предоставления услуг с целью определения их
                            качества;
                        </li>

                        <li>
                            Виконувати технічну експертизу надання послуг з метою визначення їх якості;
                        </li>

                        <li>
                            Здійснювати інспектування діяльності підприємств і організацій із захисту прав споживачів;
                        </li>

                        <li>
                            Реалізувати державну політику і політику міжнародного консьюмерського руху по захисту прав
                            споживачів.
                        </li>


                        <li>
                            Здійснювати митну технічну експертизу при оформленні експортно-імпортних вантажів;
                        </li>

                        <li>
                            Здійснювати митну технічну експертизу при оформленні експортно-імпортних вантажів;
                        </li>

                    </ol>


