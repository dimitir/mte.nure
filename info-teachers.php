            <p>Навчальний процес на кафедрі забезпечують 3 професори, 7 доцентів (к.т.н.), 1 старший викладач та 2
                асистента. Кадровий склад кафедри поповнюється за рахунок залучення провідних фахівців галузі як
                викладачів-сумісників (в тому числі 3 доктора наук), самостійної підготовки викладацьких кадрів з
                випускників аспірантури та докторантури. </p>
            <p>В підготовці з спеціальності 152 "Метрологія та інформаційно-вимірювальна техніка" кафедрі МТЕ активно допомагають
                висококваліфіковані фахівці в даній області, а також провідні вчені науково-дослідних установ різних
                напрямів. На філіалі кафедри в <a href="http://www.metrology.kharkov.ua">Національному науковому центрі "Інститут метрології"</a> 
				та в Державному підприємстві <a href="http://khsms.com">"Харківський регіональний
науково-виробничий центр стандартизації, метрології та сертифікації"</a> працює 3
                професори, доктора наук, лауреата Держпремій України і 5 доцентів, кандидатів наук.</p>
            <p>Викладачі кафедри систематично проходять підвищення кваліфікації, переважно в формі стажування на
                провідних науково-виробничих підприємствах, НДІ міста та України.</p>
            <p>Провідні фахівці кафедри активно беруть участь в підготовці науково-педагогічних кадрів вищої
                кваліфікації, здійснюють керівництво своїми аспірантами. Цей факт, а також наявність в університеті
                спеціалізованих Вчених рад по захисту кандидатських і докторських дисертацій гарантує своєчасне
                поповнення кафедри молодими кваліфікованими викладачами.</p>


			
